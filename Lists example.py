# Lists

#%%

a = [2,6,8,3]

#%%
# Extract the second element
a[1]

#%%
# Extract the first three elements
a[0:3]

#%%
# Change the third element to 5
a[2] = 5
print(a)

#%%
# Change the first element to 2 and the second element to 8
a[0:2] = [2,8]
print(a)

#%%
a = [2,6,8,3]
b = [3,6]

#%%
# Concatenate the lists
c = a + b
print(c)

#%%
# Create a new list where the values of b is repeated three times
d = b*3
print(d)



